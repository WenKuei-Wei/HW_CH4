
public class P14 {
	public static void main(String[] args) 
	{
		Car5 car1;
		car1 = new RacingCar5();
		
		car1.setCar(1234, 20.5);
		car1.show();
	}

}
class Car5
{
	protected int num;
	protected double gas;
	
	public Car5()
	{
		num = 0;
		gas = 0.0;
		System.out.println("生產了車子");
	}
	public Car5(int n,double g)
	{
		num = n;
		gas = g;
		System.out.println("生產了車號是"+num+"，油量是"+gas+"的車子");
	}
	
	public void setCar(int n,double g)
	{
		num = n;
		gas = g;
		System.out.println("車號設為"+num+"，油量設為"+gas);
	}
	public void show()
	{
		System.out.println("車號為"+num+"，油量為"+gas);
	}
}

class RacingCar5 extends Car5
{
	private int course;
	
	public RacingCar5()
	{
		course = 0;
		System.out.println("生產了賽車");
	}
	public RacingCar5(int n,double g,int c)
	{
		super(n,g);
		course = c;
		System.out.println("生產了賽車，編號為"+course);
	}
	
	public void setCourse(int c)
	{
		course = c;
		System.out.println("將賽車編號設定為"+course);
	}
	public void show()
	{
		System.out.println("車號為"+num+"，油量為"+gas);
		System.out.println("編號為"+course);
	}
}